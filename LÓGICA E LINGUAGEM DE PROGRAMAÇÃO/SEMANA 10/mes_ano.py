while True:
  numero_mes = int(input("Informe o número correspondete ao mês: "))
  print("Digite 0 para sair!")
  if(numero_mes == 0):
    print("Saindo...")
    break
  elif(numero_mes == 1):
    print("Janeiro")
  elif(numero_mes == 1):
    print("Fevereiro")
  elif(numero_mes == 2):
    print("Março")
  else:
    print("Opção inválida!")
